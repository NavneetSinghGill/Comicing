{
    return [UIColor colorWithRed:117/255.0f green:76/255.0f blue:41/255.0f alpha:1];
}

+ (UIColor *)drawingColorGreen
{
    return [UIColor colorWithRed:0/255.0f green:148.0/255.0f blue:68/255.0f alpha:1];
}

